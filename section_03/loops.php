<?php
// Example of a for loop
for ($i = 1; $i <= 10; $i++) {
  echo "The value of i is: " . $i . "\n";
}

// Example of a while loop
$j = 1;
while ($j <= 5) {
  echo "The value of j is: " . $j . "\n";
  $j++;
}

// Example of a do-while loop
$k = 1;
do {
  echo "The value of k is: " . $k . "\n";
  $k++;
} while ($k <= 3);
 